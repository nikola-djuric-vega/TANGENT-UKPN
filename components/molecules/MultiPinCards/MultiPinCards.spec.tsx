  it('should render select message', () => {
    const { getByText } = render(
      <MultiPinCards
        setIncidentState={mockSetIncidentState}
        incidentState={mockIncidentState}
      />
    )

    const selectMessage = getByText('Select an incident to view below')

    expect(selectMessage).toBeInTheDocument()
  })

  it('should render correct amount of card choice buttons', () => {
    const { queryAllByRole } = render(
      <MultiPinCards
        setIncidentState={mockSetIncidentState}
        incidentState={mockIncidentState}
      />
    )

    const buttons = queryAllByRole('button')

    const choiceButtons = buttons.filter((button) =>
      button.hasAttribute('data-is-selected')
    )

    expect(choiceButtons.length).toEqual(2)
  })

  it('should set first button to be selected on click', () => {
    const { queryAllByRole } = render(
      <MultiPinCards
        setIncidentState={mockSetIncidentState}
        incidentState={mockIncidentState}
      />
    )

    const buttons = queryAllByRole('button')

    const choiceButtons = buttons.filter((button) =>
      button.hasAttribute('data-is-selected')
    )

    fireEvent.click(choiceButtons[0])

    expect(choiceButtons[0].getAttribute('data-is-selected')).toEqual('true')
    expect(choiceButtons[1].getAttribute('data-is-selected')).toEqual('false')
  })

  it('should set second button to be selected on click', () => {
    const { queryAllByRole } = render(
      <MultiPinCards
        setIncidentState={mockSetIncidentState}
        incidentState={mockIncidentState}
      />
    )

    const buttons = queryAllByRole('button')

    const choiceButtons = buttons.filter((button) =>
      button.hasAttribute('data-is-selected')
    )

    fireEvent.click(choiceButtons[1])

    expect(choiceButtons[0].getAttribute('data-is-selected')).toEqual('false')
    expect(choiceButtons[1].getAttribute('data-is-selected')).toEqual('true')
  })

  it('should set class to pull cards up', () => {
    const { container } = render(
      <MultiPinCards
        setIncidentState={mockSetIncidentState}
        incidentState={mockIncidentState}
        pullCardsUp={true}
      />
    )

    const innerContainter = container.getElementsByClassName('componentInner')

    expect(innerContainter[0].classList.length).toBeGreaterThan(1)
  })
